import random
tryCount = 5
Numeral = random.randint(1, 1337)#25,)
print(f'Я загадал число {Numeral}')
#print(f'Я загадал число от 1 до 1337')
while tryCount != 0:
    try1 = int(input())

    if try1 == Numeral:
        print (f'Молодец, ты угадал!')
    if try1 > Numeral:
        print (f'Больше чем число')
    if try1 < Numeral:
        print (f'Меньше чем число')

    tryCount = tryCount - 1

    if tryCount == 0:
        break
